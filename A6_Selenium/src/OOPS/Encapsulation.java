package OOPS;

public class Encapsulation {

	private static int a = 10;
	private double b = 20;
	
	//getter 
	public int getA_Value() {
		return a;
	}
	
	public double getB_value() {
		return b;
	}
	
	//setter
	public void setA_value(int a) {
		this.a = a;
	}
	
	public void setB_value(double b) {
		this.b = b;
	}
	
}
